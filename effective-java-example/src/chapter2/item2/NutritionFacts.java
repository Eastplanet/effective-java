package chapter2.item2;

public class NutritionFacts {

    private int a;
    private int b;
    private int c;

    public NutritionFacts() {
    }

    public NutritionFacts(int a) {
        this.a = a;
    }

    public NutritionFacts(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public NutritionFacts(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }
}
